#Program to demonstarte addtion, subtraction, division, multiplication, modulo, floor division, exponent and assignment operator

n1=int(input())
n2=int(input())


#Addition 
temp=n1+n2
print(temp)

#Subtraction
temp=n1-n2
print(temp)

#Division 
temp=n1/n2
print(temp)

#Multiplication 
temp=n1*n2
print(temp)

#Modulo 
temp=n1%n2
print(temp)

#Floor Division 
temp=n1//n2
print(temp)

#Exponent 
temp=n1**n2
print(temp)
